LANG_IDX = {"en": 1, "es": 2, "de": 3, "it": 4}


def po_escape(s):
    return s.replace("\\", "\\\\").replace('"', '\\"')


def build_po(lang):
    lines = [
        '# Traductions %s pour ProfCalendar.' % lang,
        '# GÃĐnÃĐrÃĐes par scripts/build_translations.py â modifier CE script, pas ce fichier.',
        'msgid ""',
        'msgstr ""',
        '"Project-Id-Version: ProfCalendar\\n"',
        '"MIME-Version: 1.0\\n"',
        '"Content-Type: text/plain; charset=UTF-8\\n"',
        '"Content-Transfer-Encoding: 8bit\\n"',
        '"Language: %s\\n"' % lang,
        '"Plural-Forms: nplurals=2; plural=(n != 1);\\n"',
        '',
    ]
    for entry in ENTRIES:
        msgid = entry[0]
        msgstr = entry[LANG_IDX[lang]]
        lines.append('msgid "%s"' % po_escape(msgid))
        lines.append('msgstr "%s"' % po_escape(msgstr))
        lines.append('')
    for sing, plur, trans in PLURALS:
        t_sing, t_plur = trans[lang]
        lines.append('msgid "%s"' % po_escape(sing))
        lines.append('msgid_plural "%s"' % po_escape(plur))
        lines.append('msgstr[0] "%s"' % po_escape(t_sing))
        lines.append('msgstr[1] "%s"' % po_escape(t_plur))
        lines.append('')
    return "\n".join(lines)


def main():
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    ok = True
    for lang in LANGS:
        d = os.path.join(root, "translations", lang, "LC_MESSAGES")
        os.makedirs(d, exist_ok=True)
        po_path = os.path.join(d, "messages.po")
        mo_path = os.path.join(d, "messages.mo")
        with open(po_path, "w", encoding="utf-8") as f:
            f.write(build_po(lang))
        r = subprocess.run(["msgfmt", "--check", "-o", mo_path, po_path],
                           capture_output=True, text=True)
        if r.returncode != 0:
            print("â msgfmt %s :\n%s" % (lang, r.stderr))
            ok = False
        else:
            print("â %s : %d chaÃŪnes + %d pluriels â %s" % (lang, len(ENTRIES), len(PLURALS), mo_path))
    sys.exit(0 if ok else 1)


